public class basic_operators {
    public static void main(String[] args) {
        int a=4;
        int b=6%a; // modulo operator
        int c =9;
        c += 3;
        System.out.println(b);
        System.out.println(c);
        System.out.println(56<50);
        System.out.println(55>5 && 61>99);
        System.out.println(55>5 || 61>99);
    }
}
